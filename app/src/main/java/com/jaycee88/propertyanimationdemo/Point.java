package com.jaycee88.propertyanimationdemo;

/**
 * Point
 * Created by jaycee on 2017/8/8.
 */
public class Point {
    private float x;
    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
